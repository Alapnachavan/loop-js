// When this loop runs the first time, the value of i is 0, 0<3 is true, 
// and it is incremented due to i++, i value will become 1, therefore i is printed

// INCREMENT
for(var i = 0; i < 3; i++){
    console.log(i)
 }


//  DECREMENT
for(var n=9;n>0;n--){
    console.log(n)
 }
//  When this loop runs the first time, the value of n is 9, n>0 is true, and it is decremented due to n--, 
//  n value will become 8, therefore i is printed